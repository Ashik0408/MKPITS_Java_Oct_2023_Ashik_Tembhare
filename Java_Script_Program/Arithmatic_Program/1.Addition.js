// addition with  number
let a = 10;
let b = 20;
let c = a + b; // c is 30 addition '+'
console.log(c);

// addition with string
let f_name = "Ashik";
let l_name = "Tembhare";
console.log(f_name + l_name); // AshikTembhare

// addition with Nan, boolean , string with number
let word = "word";
let num = 1;
console.log("Word" + word + num); // Wordword1

var number = "1";
console.log(number + 2); // 12

// addition with  null and undefined
console.log(null + undefined); // NaN

// addition with boolean
console.log(1 + true); // 2
console.log(false + 1); // 1
